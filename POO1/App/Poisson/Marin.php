<?php

abstract class Marin {
  
    /**
     * Undocumented variable
     *
     * @var boolean
     */
    private $nageoires;

    /**
     * Undocumented variable
     *
     * @var boolean
     */
    private $branchies;

    /**
     * Undocumented variable
     *
     * @var string
     */
    private $couleur;

    abstract public function nager();
}
